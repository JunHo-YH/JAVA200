package chapter2;

public class SumAve1 {

	public static void main(String[] args) {
		int x; // x는 int형 변수
		int y; // y는 int형 변수

		x = 63;
		y = 18;

		System.out.println("x값은 : " + x + "입니다.");
		System.out.println("y값은 : " + y + "입니다.");
		System.out.println("합계는 : " + (x + y) + "입니다.");
		System.out.println("평균은 : " + (x + y) / 2.0 + "입니다.");
	}

}
